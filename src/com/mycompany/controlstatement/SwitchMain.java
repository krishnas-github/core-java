package com.mycompany.controlstatement;

public class SwitchMain {
    public static void main(String[] a){

        int condition = 2;
        switch (condition){

            case 1:
                System.out.println("Case 1");
                break;
            case 2:
                System.out.println("Case 2");
                break;
            case 3:
                System.out.println("Case 3");
                break;
            default:
                System.out.println("Default Case");
        }
    }
}
